

pub fn check_letter(number: &i32) -> String {

    let out_come = match number {
        1011 => "a",
        11010101 => "b",
        110101101 => "c",
        110101 => "d",
        1 => "e",
        10101101 => "f",
        1101101 => "g",
        1010101 => "h",
        101 => "i",
        1011011011 => "j",
        1101011 => "k",
        10110101 => "l",
        11011 => "m",
        1101 => "n",
        11011011 => "o",
        101101101 => "p",
        1101101011 => "q",
        101101 => "r",
        10101 => "s",
        11 => "t",
        101011 => "u",
        10101011 => "v",
        1011011 => "w",
        110101011 => "x",
        1101011011 => "y",
        110110101 => "z",
        110011 => " ",
        _ => ""
    };
    return String::from(out_come);

}
